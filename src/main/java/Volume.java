import java.util.ArrayList;

public class Volume {
    private String title;
    private String author;
    private String genre;

    public Volume(String title, String author, String genre) {
        this.title = title;
        this.author = author;
        this.genre = genre;
    }

    public String getTitle() {
        return title;
    }

    public String getAuthor() {
        return author;
    }

    public String getGenre() {
        return genre;
    }
}